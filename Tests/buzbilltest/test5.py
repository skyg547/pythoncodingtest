def dfs(x, y, n, graph, visited):
    # 방문했던 노드는 종료
    if visited[x][y]:
        return 0
    else:
        visited[x][y] = True
    # 주어진 범위를 벗어나는 경우에는 즉시 종료
    if graph[x][y] != 1:
        return 0
    if x - 1 <= -1 or x + 1 >= n or y - 1 <= -1 or y + 1 >= n:
        return 0
    cnt = 0
    # 상, 하, 좌, 우의 위치들도 모두 재귀적으로 호출
    temp1 = dfs(x - 1, y, n, graph, visited)  # 상
    temp1 += dfs(x + 1, y, n, graph, visited)  # 하

    temp2 = dfs(x, y + 1, n, graph, visited)  # 우
    temp2 += dfs(x, y - 1, n, graph, visited)  # 좌

    return min(temp1, temp2)


